package com.myjlc.loops;

//8.Find the Sum of  Numbers from 1 to N

public class Lab8 {

	public static void main(String[] args) {
int n=6;

int sum=0;
for(int i=1;i<=n;i++) {
     sum=sum+i;
} 
System.out.println("sum:"+sum);
	
	}
}

//Time complexity-O(6)
// Space complexity-O(1)
