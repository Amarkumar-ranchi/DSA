package com.myjlc.loops;

//1.Swap 2 Numbers

public class Lab1 {

	public static void main(String[] args) {
int a=10;
int b=20;

 System.out.println(a+"\t"+b);
 
int temp=a;
a=b;
b=temp;

System.out.println(a+"\t"+b); 
}

}

//Time complexity-O(1)
// Space complexity-O(1)
// 1 is constant